/**
 * YAML Generator
 *
 * Generates valid zconfig YAML from configuration objects
 */

export class YAMLGenerator {
  /**
   * Generate YAML from configuration object
   */
  static generate(config) {
    if (!config.applid) {
      return this.generateEmpty();
    }

    const sections = [];

    // Header comment
    sections.push('# CICS Region Configuration');
    sections.push(`# Generated by Bob YAML Wizard on ${new Date().toISOString()}`);
    sections.push('');

    // Main cics_region block
    sections.push('cics_region:');

    // Basic configuration
    sections.push(`  applid: ${config.applid}`);
    sections.push('  installation:');
    sections.push('    data_sets:');
    sections.push('      cics:');
    sections.push(`        hlq: ${config.cics_hlq || 'CICS.TS61'}`);
    sections.push(`  region_hlq: ${config.region_hlq || `USER.${config.applid}`}`);
    sections.push('');

    // Region JCL
    sections.push('  region_jcl:');
    sections.push('    job_parameters:');
    sections.push(`      region: ${config.memory || '512M'}`);
    sections.push('');

    // SIT Parameters
    sections.push('  sit_parameters:');
    sections.push(`    start: ${config.sit_parameters?.start || 'AUTO'}`);
    sections.push(`    cicssvc: ${config.sit_parameters?.cicssvc || 217}`);
    sections.push(`    grplist: ${config.sit_parameters?.grplist || '(DFHLIST)'}`);

    if (config.sit_parameters?.mnfreq) {
      sections.push(`    mnfreq: ${config.sit_parameters.mnfreq}`);
    }

    if (config.sit_parameters?.srbsvc) {
      sections.push(`    srbsvc: ${config.sit_parameters.srbsvc}`);
    }

    sections.push('');

    // JVM Configuration
    if (config.jvm && config.jvm.enabled) {
      sections.push('  # Java Virtual Machine Configuration');
      sections.push('  jvm:');
      sections.push(`    heap_size: ${config.jvm.heap_size || '512M'}`);
      if (config.jvm.profile) {
        sections.push(`    profile: ${config.jvm.profile}`);
      }
      sections.push('');
    }

    // Extensions (CMCI, etc.)
    const extensions = [];

    if (config.cmci && config.cmci.enabled) {
      extensions.push({
        name: 'cics_cmci',
        config: {
          port: config.cmci.port || 1490
        }
      });
    }

    if (extensions.length > 0) {
      sections.push('  # Extensions');
      sections.push('  extensions:');
      extensions.forEach(ext => {
        sections.push(`    - ${ext.name}:`);
        Object.entries(ext.config).forEach(([key, value]) => {
          sections.push(`        ${key}: ${value}`);
        });
      });
      sections.push('');
    }

    // Database Configuration (as comment for now)
    if (config.database && config.database.enabled) {
      sections.push('  # Database Configuration');
      sections.push(`  # Type: ${config.database.type || 'db2'}`);
      sections.push(`  # Connection Pool: ${config.database.connection_pool || 10}`);
      sections.push('  # Note: Database configuration requires additional setup');
      sections.push('');
    }

    // Datasets Configuration
    sections.push('  # Dataset Configuration');
    sections.push('  datasets:');

    if (config.datasets?.csd) {
      sections.push('    csd:');
      sections.push(`      primary: ${config.datasets.csd.primary || 10}`);
      sections.push(`      secondary: ${config.datasets.csd.secondary || 5}`);
    }

    if (config.datasets?.gcd) {
      sections.push('    gcd:');
      sections.push(`      primary: ${config.datasets.gcd.primary || 5}`);
      sections.push(`      secondary: ${config.datasets.gcd.secondary || 2}`);
    }

    if (config.datasets?.lcd) {
      sections.push('    lcd:');
      sections.push(`      primary: ${config.datasets.lcd.primary || 5}`);
      sections.push(`      secondary: ${config.datasets.lcd.secondary || 2}`);
    }

    return sections.join('\n');
  }

  /**
   * Generate empty/placeholder YAML
   */
  static generateEmpty() {
    return `# CICS Region Configuration
# Start chatting with Bob to build your configuration!

cics_region:
  applid: YOUR_REGION_NAME
  installation:
    data_sets:
      cics:
        hlq: CICS.TS61
  region_hlq: USER.YOUR_REGION_NAME

  region_jcl:
    job_parameters:
      region: 512M

  sit_parameters:
    start: AUTO
    cicssvc: 217
    grplist: (DFHLIST)
`;
  }

  /**
   * Generate specific section
   */
  static generateSection(sectionName, data) {
    switch (sectionName) {
      case 'jvm':
        return this.generateJVMSection(data);
      case 'cmci':
        return this.generateCMCISection(data);
      case 'database':
        return this.generateDatabaseSection(data);
      default:
        return '';
    }
  }

  /**
   * Generate JVM section
   */
  static generateJVMSection(data) {
    return `  jvm:
    heap_size: ${data.heap_size || '512M'}
    profile: ${data.profile || 'DFHJVMPR'}`;
  }

  /**
   * Generate CMCI section
   */
  static generateCMCISection(data) {
    return `  extensions:
    - cics_cmci:
        port: ${data.port || 1490}`;
  }

  /**
   * Generate Database section
   */
  static generateDatabaseSection(data) {
    return `  # Database Configuration
  # Type: ${data.type || 'db2'}
  # Connection Pool: ${data.connection_pool || 10}`;
  }

  /**
   * Add comments to YAML
   */
  static addComments(yaml, comments) {
    const lines = yaml.split('\n');
    const commentedLines = [];

    comments.forEach(comment => {
      if (comment.line && comment.line <= lines.length) {
        lines.splice(comment.line - 1, 0, `# ${comment.text}`);
      }
    });

    return lines.join('\n');
  }

  /**
   * Validate generated YAML
   */
  static validate(yaml) {
    const errors = [];
    const lines = yaml.split('\n');

    lines.forEach((line, index) => {
      // Check for tabs
      if (line.includes('\t')) {
        errors.push({
          line: index + 1,
          message: 'YAML does not allow tabs'
        });
      }

      // Check for trailing spaces
      if (line.endsWith(' ') && line.trim() !== '') {
        errors.push({
          line: index + 1,
          message: 'Trailing spaces detected'
        });
      }
    });

    return {
      valid: errors.length === 0,
      errors: errors
    };
  }

  /**
   * Format YAML with proper indentation
   */
  static format(yaml) {
    const lines = yaml.split('\n');
    const formatted = [];
    let currentIndent = 0;

    lines.forEach(line => {
      const trimmed = line.trim();

      if (trimmed === '') {
        formatted.push('');
        return;
      }

      if (trimmed.startsWith('#')) {
        formatted.push(line);
        return;
      }

      // Calculate indent based on structure
      const indent = '  '.repeat(currentIndent);
      formatted.push(indent + trimmed);
    });

    return formatted.join('\n');
  }

  /**
   * Generate template YAML
   */
  static generateTemplate(templateName) {
    const templates = {
      basic: {
        applid: 'BASIC01',
        memory: '256M'
      },
      java: {
        applid: 'JAVAPROD',
        memory: '1G',
        jvm: {
          enabled: true,
          heap_size: '512M'
        }
      },
      management: {
        applid: 'MGMT01',
        memory: '512M',
        cmci: {
          enabled: true,
          port: 1490
        }
      },
      production: {
        applid: 'PROD01',
        memory: '2G',
        jvm: {
          enabled: true,
          heap_size: '1G'
        },
        cmci: {
          enabled: true,
          port: 1490
        },
        sit_parameters: {
          start: 'AUTO',
          cicssvc: 217,
          grplist: '(DFHLIST)',
          mnfreq: '0500'
        }
      }
    };

    const template = templates[templateName];
    if (template) {
      return this.generate(template);
    }

    return this.generateEmpty();
  }

  /**
   * Convert YAML to JSON
   */
  static toJSON(yaml) {
    // This would require a YAML parser
    // For now, return a placeholder
    return {
      success: false,
      message: 'YAML to JSON conversion requires parser'
    };
  }

  /**
   * Get YAML statistics
   */
  static getStatistics(yaml) {
    const lines = yaml.split('\n');
    const nonEmptyLines = lines.filter(l => l.trim() !== '');
    const commentLines = lines.filter(l => l.trim().startsWith('#'));
    const codeLines = nonEmptyLines.length - commentLines.length;

    return {
      totalLines: lines.length,
      codeLines: codeLines,
      commentLines: commentLines.length,
      emptyLines: lines.length - nonEmptyLines.length,
      characters: yaml.length
    };
  }
}

